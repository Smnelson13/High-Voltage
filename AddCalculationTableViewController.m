}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{

  return self.calculations.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"CalculationsCell" forIndexPath:indexPath];
    
  NSString *aCalculation = self.calculations[indexPath.row];
  cell.textLabel.text = aCalculation;
  
  
    return cell;
}


- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
  [tableView deselectRowAtIndexPath:indexPath animated:YES];
  NSString *selectedCalculation = self.calculations[indexPath.row];
  [self.delegate calculationWasChosen: selectedCalculation];
  

}


